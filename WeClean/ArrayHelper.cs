using System;
using System.Collections.Generic;
using System.Security.Cryptography;

namespace WeClean
{
    public static class ArrayHelper
    {
        public static void Shuffle<T>(this IList<T> list, Random rng)
        {
            int n = list.Count;
            while (n > 1)
            {
                int k = rng.Next(n--);
                T temp = list[n];
                list[n] = list[k];
                list[k] = temp;
            }
        }
    }
}